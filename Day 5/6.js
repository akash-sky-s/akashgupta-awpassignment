function handleMouseover() {
    let element = document.querySelector("html").children[1].children[0].children[0];
    element.style.color = "snow";

    element.innerHTML = "Looking Cool..!!";
}

function handleMouseout() {
    let element = document.querySelector("html").children[1].children[0].children[0];
    element.style.color = "snow";

    element.innerHTML = "Enjoy Your Day..!!";
}